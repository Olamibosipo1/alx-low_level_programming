#include "main.h"
/**
 * print_alphabet_x10 - 10 x alphabet
 * void: void print_alphabet_x10(void)
 *
 * return : 0 if success
 **/

void print_alphabet_x10(void)
{
	char x, y;

	while
		(x = 'a')
		(x <= 'z');
		 x++;
	{
		_putchar(x);
	}

	while
		(y = '0')
		(y <= '9');
		y++;
	{

		_putchar(x);
	}
}
